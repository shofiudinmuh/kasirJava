    private void kd_memKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kd_memKeyPressed
        // TODO add your handling code here:

        km=kd_mem.getText();
        int tekanenter=evt.getKeyCode();
        if (tekanenter==10){
            try {
                sql="SELECT * FROM member "
                +"where kd_member='"+ km +"'";
                st=con.createStatement();
                RSmember=st.executeQuery(sql);
                while (RSmember.next()){
                    nama.setText(RSmember.getString("nama"));
                    no_tlp.setText(RSmember.getString(3));
                    alamat.setText(RSmember.getString(4));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "ERROR \n Data Tidak Ada / Tidak Ditemukan" +e.getMessage());
                //nm_brg.requestFocus();
            }
        }
    }//GEN-LAST:event_kd_memKeyPressed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:

        km=String.valueOf(kd_mem.getText());
        nm=String.valueOf(nama.getText());
        tlp=String.valueOf(no_tlp.getText());
        almt=String.valueOf(alamat.getText());

        try{
            sql="INSERT INTO member (kd_member, nama, no_tlp, alamat)value"
            + "('"+ km +"','"+ nm +"','"+ tlp +"','"+ almt +"')";
            st=con.createStatement();
            st.execute(sql);
            Clear();
            ShowData(sql);
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR \n"+e.getMessage());
        }
    }//GEN-LAST:event_SaveActionPerformed

