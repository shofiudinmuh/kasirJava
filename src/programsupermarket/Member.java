    private void ShowData(String sql){
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn ("No");
        data.addColumn ("Kode Member");
        data.addColumn ("Nama");
        data.addColumn ("No Tlp");
        data.addColumn ("Alamat");
        try{    
            int i = 1;
            st=con.createStatement();
            RSmember=st.executeQuery("select * from member");
            while (RSmember.next())
                data.addRow(new Object[]{
                (""+i++),
                    RSmember.getString(1), RSmember.getString(2),
                    RSmember.getString(3), RSmember.getString(4)
                });
            tabelmember.setModel(data);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "ERROR \n Gagal Memuat KeDatabase \n Aktifkan Database Sebelum Memulai");
        }
    }
    
